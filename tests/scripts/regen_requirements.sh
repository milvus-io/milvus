#!/usr/bin/env bash
# Regenerate requirements.txt from pyproject.toml + uv.lock for a given project.
#
# Usage:
#   bash tests/scripts/regen_requirements.sh <project-dir> [--check]
#
# Without --check: writes requirements.txt in <project-dir>.
# With --check:    writes to a temp file and diffs against committed requirements.txt.
#                  Exits non-zero if they diverge.
#
# This script is the single source of truth for how requirements.txt is produced;
# CI runs it in --check mode to gate PRs.
set -euo pipefail

if [[ $# -lt 1 ]]; then
    echo "usage: $0 <project-dir> [--check]" >&2
    exit 2
fi

PROJECT_DIR="$(cd "$1" 2>/dev/null && pwd || true)"
MODE="${2:-write}"

if [[ -z "$PROJECT_DIR" || ! -f "$PROJECT_DIR/pyproject.toml" ]]; then
    echo "error: '$1' does not contain a pyproject.toml" >&2
    exit 2
fi

HEADER=$(cat <<'EOF'
# DO NOT EDIT. Generated by tests/scripts/regen_requirements.sh
# from pyproject.toml + uv.lock.
#
# To add/remove deps, edit pyproject.toml then run:
#   cd <this-project-dir>
#   uv add <pkg>                      # or: uv add --group dev <pkg>
#   bash ../scripts/regen_requirements.sh .
#   git add pyproject.toml uv.lock requirements.txt
#
# pymilvus rc builds are hosted on test.pypi.org; we keep --extra-index-url
# so plain `pip install -r requirements.txt` (used by Dockerfile / Jenkins)
# can still resolve them.
--extra-index-url https://test.pypi.org/simple/

EOF
)

generate() {
    local out_file="$1"
    {
        echo "$HEADER"
        cd "$PROJECT_DIR"
        uv export \
            --format requirements-txt \
            --no-hashes \
            --no-emit-project \
            --no-header \
            --all-groups
    } > "$out_file"
}

if [[ "$MODE" == "--check" ]]; then
    TMP=$(mktemp)
    trap 'rm -f "$TMP"' EXIT
    generate "$TMP"
    if ! diff -u "$PROJECT_DIR/requirements.txt" "$TMP"; then
        echo ""
        echo "::error::$PROJECT_DIR/requirements.txt is out of sync with pyproject.toml/uv.lock."
        echo "Run 'bash tests/scripts/regen_requirements.sh $PROJECT_DIR' and commit the result."
        exit 1
    fi
else
    generate "$PROJECT_DIR/requirements.txt"
    echo "Wrote $PROJECT_DIR/requirements.txt"
fi
