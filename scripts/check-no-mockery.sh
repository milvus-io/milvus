#!/usr/bin/env bash
set -euo pipefail

target="${1:-.}"

failed=0

check() {
  local desc="$1"
  shift
  if "$@"; then
    return 0
  fi
  echo "mockery check failed: ${desc}" >&2
  failed=1
}

check "testify/mock import under ${target}" \
  bash -c 'git grep -n "github.com/stretchr/testify/mock" -- "$1" >/tmp/mockery-check-testify.out 2>/dev/null; test $? -eq 1' _ "$target"

check "mockery generated code under ${target}" \
  bash -c 'git grep -n "Code generated by mockery" -- "$1" >/tmp/mockery-check-generated.out 2>/dev/null; test $? -eq 1' _ "$target"

check "mockery expectation APIs under ${target}" \
  bash -c 'git grep -n -E "\.On\(|\.EXPECT\(\)|AssertExpectations|NewMock" -- "$1" >/tmp/mockery-check-api.out 2>/dev/null; test $? -eq 1' _ "$target"

if [[ "$failed" -ne 0 ]]; then
  for f in /tmp/mockery-check-*.out; do
    [[ -s "$f" ]] && cat "$f" >&2
  done
  exit 1
fi

echo "No mockery usage under ${target}"
