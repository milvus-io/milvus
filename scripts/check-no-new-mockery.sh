#!/usr/bin/env bash
set -euo pipefail

target="${2:-${MOCKERY_CHECK_TARGET:-HEAD}}"
base="${1:-${MOCKERY_CHECK_BASE:-}}"

pathspecs=(
  ":(glob)**/*.go"
  ":(glob)**/*.yaml"
  ":(glob)**/*.yml"
  "Makefile"
  ":(glob)**/Makefile"
)

forbidden_line_pattern='github\.com/stretchr/testify/mock|Code generated by mockery|go:generate[[:space:]]+mockery|(^|[[:space:]/])mockery([[:space:]]|$)|\.EXPECT\(|\.On\(|AssertExpectations|NewMock'
forbidden_path_pattern='(^|/)mock_.*\.go$|(^|/)mocks?/.*\.go$'

resolve_base() {
  if [[ -n "${base}" ]]; then
    echo "${base}"
    return
  fi

  local candidate
  local base_ref="${CHANGE_TARGET:-${GITHUB_BASE_REF:-}}"
  if [[ -n "${base_ref}" ]]; then
    for candidate in "milvus/${base_ref}" "origin/${base_ref}" "upstream/${base_ref}" "${base_ref}"; do
      if git rev-parse --verify --quiet "${candidate}^{commit}" >/dev/null; then
        echo "${candidate}"
        return
      fi
    done
  fi

  for candidate in milvus/master origin/master upstream/master "@{u}"; do
    if git rev-parse --verify --quiet "${candidate}^{commit}" >/dev/null; then
      echo "${candidate}"
      return
    fi
  done

  echo ""
}

check_diff() {
  local desc="$1"
  shift

  local tmpdir
  tmpdir="$(mktemp -d)"
  trap 'rm -rf "${tmpdir}"' RETURN

  git diff --name-status --diff-filter=ACMR "$@" -- "${pathspecs[@]}" >"${tmpdir}/names"
  awk '{print $NF}' "${tmpdir}/names" | grep -n -E "${forbidden_path_pattern}" >"${tmpdir}/paths" || true

  git diff --unified=0 --diff-filter=ACMR "$@" -- "${pathspecs[@]}" |
    awk '
      /^diff --git / {
        file = $4
        sub(/^b\//, "", file)
        next
      }
      /^\+\+\+/ {
        next
      }
      /^\+/ {
        print file ":" substr($0, 2)
      }
    ' >"${tmpdir}/added-lines"

  grep -n -E "${forbidden_line_pattern}" "${tmpdir}/added-lines" >"${tmpdir}/lines" || true

  if [[ -s "${tmpdir}/paths" || -s "${tmpdir}/lines" ]]; then
    echo "mockery check failed: new mockery usage found in ${desc}" >&2
    if [[ -s "${tmpdir}/paths" ]]; then
      echo "forbidden new mock file path:" >&2
      cat "${tmpdir}/paths" >&2
    fi
    if [[ -s "${tmpdir}/lines" ]]; then
      echo "forbidden added line:" >&2
      cat "${tmpdir}/lines" >&2
    fi
    exit 1
  fi
}

check_untracked() {
  local tmpdir
  tmpdir="$(mktemp -d)"
  trap 'rm -rf "${tmpdir}"' RETURN

  git ls-files --others --exclude-standard -- "${pathspecs[@]}" >"${tmpdir}/names"
  grep -n -E "${forbidden_path_pattern}" "${tmpdir}/names" >"${tmpdir}/paths" || true

  while IFS= read -r file; do
    awk -v file="${file}" '{print file ":" $0}' "${file}"
  done <"${tmpdir}/names" >"${tmpdir}/added-lines"

  grep -n -E "${forbidden_line_pattern}" "${tmpdir}/added-lines" >"${tmpdir}/lines" || true

  if [[ -s "${tmpdir}/paths" || -s "${tmpdir}/lines" ]]; then
    echo "mockery check failed: new mockery usage found in untracked files" >&2
    if [[ -s "${tmpdir}/paths" ]]; then
      echo "forbidden new mock file path:" >&2
      cat "${tmpdir}/paths" >&2
    fi
    if [[ -s "${tmpdir}/lines" ]]; then
      echo "forbidden added line:" >&2
      cat "${tmpdir}/lines" >&2
    fi
    exit 1
  fi
}

resolved_base="$(resolve_base)"
if [[ -n "${resolved_base}" ]]; then
  if git merge-base --is-ancestor "${resolved_base}" "${target}" 2>/dev/null; then
    check_diff "${resolved_base}...${target}" "${resolved_base}...${target}"
  else
    check_diff "${resolved_base}..${target}" "${resolved_base}..${target}"
  fi
else
  echo "mockery check failed: no base ref found" >&2
  echo "Pass a base ref or set MOCKERY_CHECK_BASE." >&2
  exit 1
fi

if [[ "${target}" == "HEAD" ]]; then
  check_diff "staged changes" --cached
  check_diff "unstaged changes"
  check_untracked
fi

echo "No new mockery usage found"
