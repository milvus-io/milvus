SOURCE="${BASH_SOURCE[0]}"
while [ -h "$SOURCE" ]; do # resolve $SOURCE until the file is no longer a symlink
  DIR="$( cd -P "$( dirname "$SOURCE" )" && pwd )"
  SOURCE="$(readlink "$SOURCE")"
  [[ $SOURCE != /* ]] && SOURCE="$DIR/$SOURCE" # if $SOURCE was a relative symlink, we need to resolve it relative to the path where the symlink file was located
done
ROOT_DIR="$( cd -P "$( dirname "$SOURCE" )/.." && pwd )"

CPP_SRC_DIR="${ROOT_DIR}/internal/core"

pushd $CPP_SRC_DIR
./run_clang_format.sh .
popd

check_result=$(git status | grep -E "\.h\|\.hpp\|\.cc\|\.cpp")
echo "check_result: $check_result"
if test -z "$check_result"; then
  exit 0
else
  echo "The cpp files are not formatted, please use internal/core/run_clang_format.sh"
  exit 1
fi
