package storage

import (
	"encoding/binary"
	"fmt"

	"github.com/cockroachdb/errors"
)

const (
	// LOBReferenceSize is the fixed size of binary encoded LOB reference (16 bytes)
	LOBReferenceSize = 16
)

// LOBReference represents a reference to LOB (Large Object) data stored in a separate file.
// It uses a fixed-length binary encoding (16 bytes) for efficient storage and processing.
//
// Structure:
// - LobFileID: uint64 (8 bytes) - The LOB file identifier generated by TSO allocator
// - RowOffset: uint32 (4 bytes) - Row offset within the LOB file
// - Reserved:  uint32 (4 bytes) - Reserved for future extensions
type LOBReference struct {
	LobFileID uint64 // LOB file ID generated by allocator.Alloc()
	RowOffset uint32 // Row offset in the LOB file
	Reserved  uint32 // Reserved field for future use
}

func NewLOBReference(lobFileID uint64, rowOffset uint32) *LOBReference {
	return &LOBReference{
		LobFileID: lobFileID,
		RowOffset: rowOffset,
		Reserved:  0,
	}
}

// Format: [LobFileID:8bytes][RowOffset:4bytes][Reserved:4bytes]
func EncodeLOBReference(ref *LOBReference) []byte {
	buf := make([]byte, LOBReferenceSize)
	binary.LittleEndian.PutUint64(buf[0:8], ref.LobFileID)
	binary.LittleEndian.PutUint32(buf[8:12], ref.RowOffset)
	binary.LittleEndian.PutUint32(buf[12:16], ref.Reserved)
	return buf
}

func DecodeLOBReference(data []byte) (*LOBReference, error) {
	if len(data) != LOBReferenceSize {
		return nil, errors.Newf("invalid LOB reference size: expected %d bytes, got %d bytes", LOBReferenceSize, len(data))
	}

	return &LOBReference{
		LobFileID: binary.LittleEndian.Uint64(data[0:8]),
		RowOffset: binary.LittleEndian.Uint32(data[8:12]),
		Reserved:  binary.LittleEndian.Uint32(data[12:16]),
	}, nil
}

func (r *LOBReference) String() string {
	return fmt.Sprintf("lob:%d:%d", r.LobFileID, r.RowOffset)
}

func (r *LOBReference) IsValid() bool {
	return r.LobFileID > 100
}

// LOBReferenceFromString parses a human-readable LOB reference string.
// This is mainly for debugging and testing purposes.
// Format: "lob:{fileID}:{offset}"
func LOBReferenceFromString(s string) (*LOBReference, error) {
	var fileID uint64
	var offset uint32
	n, err := fmt.Sscanf(s, "lob:%d:%d", &fileID, &offset)
	if err != nil || n != 2 {
		return nil, errors.Newf("invalid LOB reference string format: %s", s)
	}
	return &LOBReference{
		LobFileID: fileID,
		RowOffset: offset,
		Reserved:  0,
	}, nil
}
