inline constexpr size_t kFastMemcpyMaxSize = MILVUS_FASTCPY_MAX_SIZE;
inline constexpr size_t kFastMemcpyDispatchMaxSize = 1024;
static_assert(kFastMemcpyMaxSize <= kFastMemcpyDispatchMaxSize,
              "MILVUS_FASTCPY_MAX_SIZE must be <= 1024");

#if (defined(__linux__) || defined(__APPLE__)) &&                         \
    (defined(__x86_64__) || defined(__amd64__) || defined(__aarch64__) || \
     defined(__arm64__))
inline constexpr bool kFastMemcpyPlatformSupported = true;
#else
inline constexpr bool kFastMemcpyPlatformSupported = false;
#endif
inline constexpr bool kFastMemcpyCompiled =
    kFastMemcpyPlatformSupported && kFastMemcpyMaxSize != 0;

template <size_t Size>
inline void
MemcpyFixed(void* dst, const void* src) noexcept {
    if constexpr (Size != 0) {
        std::memcpy(dst, src, Size);
    }
}

inline bool
FastMemcpyEnabled() noexcept {
    return kFastMemcpyCompiled;
}

inline bool
IsNonOverlapping(const void* dst, const void* src, size_t size) noexcept {
    auto dst_begin = reinterpret_cast<uintptr_t>(dst);
    auto src_begin = reinterpret_cast<uintptr_t>(src);
    return size == 0 || dst_begin + size <= src_begin ||
           src_begin + size <= dst_begin;
}

inline void
AssertNonOverlapping(void* dst, const void* src, size_t size) noexcept {
#ifndef NDEBUG
    assert(IsNonOverlapping(dst, src, size));
#endif
}

