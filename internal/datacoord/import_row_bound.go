package datacoord

import (
	"strconv"

	"github.com/milvus-io/milvus-proto/go-api/v3/schemapb"
	"github.com/milvus-io/milvus/pkg/v3/util/merr"
	"github.com/milvus-io/milvus/pkg/v3/util/typeutil"
)

// fixedBinaryItemSize returns the exact fixed binary itemsize, in bytes, that one
// value of the field occupies in a numpy/binary source file. For variable-length
// scalar types (VarChar/JSON) it returns the declared max_length upper bound
// (fallback 1 when unset/unparseable). Vector sizes are derived from the field dim.
func fixedBinaryItemSize(field *schemapb.FieldSchema) (int64, error) {
	switch field.GetDataType() {
	case schemapb.DataType_Bool, schemapb.DataType_Int8:
		return 1, nil
	case schemapb.DataType_Int16:
		return 2, nil
	case schemapb.DataType_Int32, schemapb.DataType_Float:
		return 4, nil
	case schemapb.DataType_Int64, schemapb.DataType_Double:
		return 8, nil
	case schemapb.DataType_VarChar:
		return varCharMaxLength(field), nil
	case schemapb.DataType_FloatVector:
		dim, err := typeutil.GetDim(field)
		if err != nil {
			return 0, err
		}
		return 4 * dim, nil
	case schemapb.DataType_Float16Vector, schemapb.DataType_BFloat16Vector:
		dim, err := typeutil.GetDim(field)
		if err != nil {
			return 0, err
		}
		return 2 * dim, nil
	case schemapb.DataType_Int8Vector:
		dim, err := typeutil.GetDim(field)
		if err != nil {
			return 0, err
		}
		return dim, nil
	case schemapb.DataType_BinaryVector:
		dim, err := typeutil.GetDim(field)
		if err != nil {
			return 0, err
		}
		return dim / 8, nil
	default:
		return 0, merr.WrapErrParameterInvalidMsg("unsupported data type %s for fixed binary item size", field.GetDataType().String())
	}
}

// varCharMaxLength reads the declared max_length TypeParam of a VarChar field,
// falling back to 1 when the param is missing or cannot be parsed.
func varCharMaxLength(field *schemapb.FieldSchema) int64 {
	for _, kv := range field.GetTypeParams() {
		if kv.GetKey() == "max_length" {
			v, err := strconv.ParseInt(kv.GetValue(), 10, 64)
			if err != nil || v <= 0 {
				return 1
			}
			return v
		}
	}
	return 1
}

// numpyRowByteSize returns the sum of the exact fixed binary itemsize (bytes per
// row) over the stored fields of the schema, EXCLUDING an autoID primary key
// (which is generated by Milvus and not present in the source files). It errors
// if the resulting sum is <= 0.
func numpyRowByteSize(schema *schemapb.CollectionSchema) (int64, error) {
	var total int64
	for _, field := range schema.GetFields() {
		if field.GetIsPrimaryKey() && field.GetAutoID() {
			continue
		}
		size, err := fixedBinaryItemSize(field)
		if err != nil {
			return 0, err
		}
		total += size
	}
	if total <= 0 {
		return 0, merr.WrapErrParameterInvalidMsg("non-positive per-row byte size %d derived from schema", total)
	}
	return total, nil
}

// minRowTextBytes returns a provable lower bound on the number of bytes one row
// occupies when serialized as JSON/CSV, EXCLUDING an autoID primary key. Vector
// fields contribute at least one character per element (dim, or dim/8 for binary
// vectors); VarChar/JSON may be empty and contribute 0; every other scalar
// contributes at least 1. The result is floored to 1 to avoid divide-by-zero
// downstream.
func minRowTextBytes(schema *schemapb.CollectionSchema) (int64, error) {
	var total int64
	for _, field := range schema.GetFields() {
		if field.GetIsPrimaryKey() && field.GetAutoID() {
			continue
		}
		switch field.GetDataType() {
		case schemapb.DataType_VarChar, schemapb.DataType_JSON:
			// May be empty, contributes 0.
		case schemapb.DataType_FloatVector, schemapb.DataType_Float16Vector,
			schemapb.DataType_BFloat16Vector, schemapb.DataType_Int8Vector:
			dim, err := typeutil.GetDim(field)
			if err != nil {
				return 0, err
			}
			total += dim
		case schemapb.DataType_BinaryVector:
			dim, err := typeutil.GetDim(field)
			if err != nil {
				return 0, err
			}
			total += dim / 8
		default:
			total++
		}
	}
	if total < 1 {
		total = 1
	}
	return total, nil
}
