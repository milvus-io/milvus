// fixedBinaryItemSize returns the exact fixed binary itemsize, in bytes, that one
// value of the field occupies in a numpy/binary source file. For variable-length
// scalar types (VarChar/JSON) it returns the declared max_length upper bound
// (fallback 1 when unset/unparseable). Vector sizes are derived from the field dim.
func fixedBinaryItemSize(field *schemapb.FieldSchema) (int64, error) {
	switch field.GetDataType() {
	case schemapb.DataType_Bool, schemapb.DataType_Int8:
		return 1, nil
	case schemapb.DataType_Int16:
		return 2, nil
	case schemapb.DataType_Int32, schemapb.DataType_Float:
		return 4, nil
	case schemapb.DataType_Int64, schemapb.DataType_Double:
		return 8, nil
	case schemapb.DataType_VarChar:
		return varCharMaxLength(field), nil
	case schemapb.DataType_FloatVector:
		dim, err := typeutil.GetDim(field)
		if err != nil {
			return 0, err
		}
		return 4 * dim, nil
	case schemapb.DataType_Float16Vector, schemapb.DataType_BFloat16Vector:
		dim, err := typeutil.GetDim(field)
		if err != nil {
			return 0, err
		}
		return 2 * dim, nil
	case schemapb.DataType_Int8Vector:
		dim, err := typeutil.GetDim(field)
		if err != nil {
			return 0, err
		}
		return dim, nil
	case schemapb.DataType_BinaryVector:
		dim, err := typeutil.GetDim(field)
		if err != nil {
			return 0, err
		}
		return dim / 8, nil
	default:
		return 0, merr.WrapErrParameterInvalidMsg("unsupported data type %s for fixed binary item size", field.GetDataType().String())
	}
}

// varCharMaxLength reads the declared max_length TypeParam of a VarChar field,
// falling back to 1 when the param is missing or cannot be parsed.
func varCharMaxLength(field *schemapb.FieldSchema) int64 {
	for _, kv := range field.GetTypeParams() {
		if kv.GetKey() == "max_length" {
			v, err := strconv.ParseInt(kv.GetValue(), 10, 64)
			if err != nil || v <= 0 {
				return 1
			}
			return v
		}
	}
	return 1
}

// nonSourceFieldIDs returns field IDs absent from import source files: the
// autoID primary key (generated by Milvus) and all function-output fields
// (generated during import, e.g. a BM25 sparse vector).
func nonSourceFieldIDs(schema *schemapb.CollectionSchema) typeutil.Set[int64] {
	ids := typeutil.NewSet[int64]()
	for _, f := range schema.GetFields() {
		if f.GetIsPrimaryKey() && f.GetAutoID() {
			ids.Insert(f.GetFieldID())
		}
	}
	for _, fn := range schema.GetFunctions() {
		ids.Insert(fn.GetOutputFieldIds()...)
	}
	return ids
}

// numpyRowByteSize returns the sum of the exact fixed binary itemsize (bytes per
// row) over the source fields of the schema, EXCLUDING fields not present in the
// source files (autoID primary key and function-output fields). Fields whose size
// cannot be derived (e.g. an unknown/variable type) are skipped rather than
// treated as an error. A resulting total of 0 signals the caller to fall back to
// the byte bound and is returned as (0, nil).
func numpyRowByteSize(schema *schemapb.CollectionSchema) (int64, error) {
	skip := nonSourceFieldIDs(schema)
	var total int64
	for _, field := range schema.GetFields() {
		if skip.Contain(field.GetFieldID()) {
			continue
		}
		size, err := fixedBinaryItemSize(field)
		if err != nil {
			// Unknown/variable source field (e.g. a user-provided SparseFloatVector):
			// its per-row size is not provable, so skip it rather than erroring.
			continue
		}
		total += size
	}
	if total <= 0 {
		return 0, nil
	}
	return total, nil
}

// minRowTextBytes returns a provable lower bound on the number of bytes one row
// occupies when serialized as JSON/CSV, EXCLUDING fields not present in the source
// files (autoID primary key and function-output fields). Known dense vector fields
// contribute at least one character per element (dim, or dim/8 for binary vectors);
// VarChar/JSON may be empty and contribute 0; unknown/sparse source fields
// contribute 0; every other known fixed scalar contributes at least 1. The result
// is floored to 1 to avoid divide-by-zero downstream.
func minRowTextBytes(schema *schemapb.CollectionSchema) (int64, error) {
	skip := nonSourceFieldIDs(schema)
	var total int64
	for _, field := range schema.GetFields() {
		if skip.Contain(field.GetFieldID()) {
			continue
		}
		switch field.GetDataType() {
		case schemapb.DataType_Bool, schemapb.DataType_Int8, schemapb.DataType_Int16,
			schemapb.DataType_Int32, schemapb.DataType_Int64,
			schemapb.DataType_Float, schemapb.DataType_Double:
			// Known fixed scalar: at least 1 character.
			total++
		case schemapb.DataType_FloatVector, schemapb.DataType_Float16Vector,
			schemapb.DataType_BFloat16Vector, schemapb.DataType_Int8Vector:
			dim, err := typeutil.GetDim(field)
			if err != nil {
				// Unknown dimension: cannot prove a floor, contributes 0.
				continue
			}
			total += dim
		case schemapb.DataType_BinaryVector:
			dim, err := typeutil.GetDim(field)
			if err != nil {
				continue
			}
			total += dim / 8
		default:
			// VarChar/JSON may be empty; unknown/sparse source fields contribute 0.
		}
	}
	if total < 1 {
		total = 1
	}
	return total, nil
}

// computeFileRowUpperBound returns a provable upper bound on the row count of one
// ImportFile, capped at cap (cap <= 0 means uncapped). Parquet is exact (footer);
// numpy/text divide Σ file size by a provable per-row byte floor; if that floor is
// unknown (0), it falls back to Σ file size (>= 1 byte per row).
func computeFileRowUpperBound(ctx context.Context, cm storage.ChunkManager,
	schema *schemapb.CollectionSchema, file *internalpb.ImportFile, cap int64,
) (int64, error) {
	ft, err := importutilv2.GetFileType(file)
	if err != nil {
		return 0, err
	}

	sumSize := func() (int64, error) {
		var total int64
		for _, path := range file.GetPaths() {
			size, err := cm.Size(ctx, path)
			if err != nil {
				return 0, err
			}
			total += size
		}
		return total, nil
	}

	var bound int64
	switch ft {
	case importutilv2.Parquet:
		bound, err = parquetNumRows(ctx, cm, file.GetPaths()[0])
		if err != nil {
			return 0, err
		}
	case importutilv2.Numpy:
		rowBytes, _ := numpyRowByteSize(schema)
		total, err := sumSize()
		if err != nil {
			return 0, err
		}
		if rowBytes > 0 {
			bound = total/rowBytes + 1
		} else {
			bound = total
		}
	case importutilv2.JSON, importutilv2.CSV, importutilv2.JSONLines:
		minRow, _ := minRowTextBytes(schema)
		total, err := sumSize()
		if err != nil {
			return 0, err
		}
		bound = total/minRow + 1
	default:
		return 0, merr.WrapErrImportFailed("unknown import file type for PK range sizing")
	}

	if bound < 0 {
		bound = 0
	}
	if cap > 0 && bound > cap {
		bound = cap
	}
	return bound, nil
}

// parquetNumRows reads only the parquet footer to get the exact row count.
func parquetNumRows(ctx context.Context, cm storage.ChunkManager, path string) (int64, error) {
	cmReader, err := cm.Reader(ctx, path)
	if err != nil {
		return 0, merr.WrapErrImportSysFailedMsg("open parquet reader failed, err=%v", err)
	}
	retryableReader := importutilv2common.NewRetryableReader(ctx, path, cmReader)
	defer retryableReader.Close()
	pr, err := file.NewParquetReader(retryableReader)
	if err != nil {
		return 0, merr.WrapErrImportSysFailedMsg("new parquet reader failed, err=%v", err)
	}
	defer pr.Close()
	return pr.NumRows(), nil
}
